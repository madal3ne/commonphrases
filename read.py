cleaned = re.sub(r'[^A-Z]', '', text.upper())

def best_ngrams(text, max_n=7, min_freq=2):
    ngram_freq = defaultdict(Counter)
    for n in range(1, max_n+1):
        for i in range(len(text) - n + 1):
            gram = text[i:i+n]
            ngram_freq[n][gram] += 1

    best = {}
    for n in sorted(ngram_freq.keys()):
        filtered = [(g, c) for g, c in ngram_freq[n].most_common() if c >= min_freq]
        if filtered:
            best[n] = filtered
    return best

top_grams = best_ngrams(cleaned, max_n=7, min_freq=5)

